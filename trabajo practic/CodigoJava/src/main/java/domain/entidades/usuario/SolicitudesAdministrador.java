package domain.entidades.usuario;

import javax.persistence.*;

@Entity
@Table(name = "solicitudesAdministrador")
public class SolicitudesAdministrador {
    @Id
    @GeneratedValue
    private int id_solicitudesAdministrador;

    @Column(name = "nombre")
    private String nombre;

    @Column(name = "apellido")
    private String apellido;

    @Column(name = "edad")
    private int edad;

    @Column(name = "domicilio")
    private String domicilio;

    @Column(name = "nacimiento")
    private String nacimiento;

    @Enumerated(EnumType.STRING)
    protected TipoDeDocumento tipoDeDocumento;

    @Column(name = "numeroDeDocumento")
    private String numeroDocumento;

    @Column(name = "numeroDeTramite")
    private String numeroTramite;

    @Column(name = "experiencia")
    private String experiencia;

    @Column(name = "email")
    private  String email;

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public int getId() {
        return id_solicitudesAdministrador;
    }

    public void setId(int id) {
        this.id_solicitudesAdministrador = id;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getApellido() {
        return apellido;
    }

    public void setApellido(String apellido) {
        this.apellido = apellido;
    }

    public int getEdad() {
        return edad;
    }

    public void setEdad(int edad) {
        this.edad = edad;
    }

    public String getDomicilio() {
        return domicilio;
    }

    public void setDomicilio(String domicilio) {
        this.domicilio = domicilio;
    }

    public String getNacimiento() {
        return nacimiento;
    }

    public void setNacimiento(String nacimiento) {
        this.nacimiento = nacimiento;
    }

    public TipoDeDocumento getTipoDeDocumento() {
        return tipoDeDocumento;
    }

    public void setTipoDeDocumento(TipoDeDocumento tipoDeDocumento) {
        this.tipoDeDocumento = tipoDeDocumento;
    }

    public String getNumeroDocumento() {
        return numeroDocumento;
    }

    public void setNumeroDocumento(String numeroDocumento) {
        this.numeroDocumento = numeroDocumento;
    }

    public String getNumeroTramite() {
        return numeroTramite;
    }

    public void setNumeroTramite(String numeroTramite) {
        this.numeroTramite = numeroTramite;
    }

    public String getExperiencia() {
        return experiencia;
    }

    public void setExperiencia(String experiencia) {
        this.experiencia = experiencia;
    }
}